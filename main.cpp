#include <iostream>

#include <cfloat>

using namespace std;

int main()
{
	cout << sizeof(char) << "bytes" << endl;
    
    cout << sizeof(int) << "bytes" << endl;
    
    cout << sizeof(unsigned int) << "bytes" << endl;
    
    cout << sizeof(short) << "bytes" << endl;
    
    cout << sizeof(long) << "bytes" << endl;
    
    cout << sizeof(long long) << "bytes" << endl;
	return 0;
}
